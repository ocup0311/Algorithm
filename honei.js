function honei(num, first, second, third) {
  let step = [0, 1, 3, 7, 15]

  // 發想：
  // 有空 1.就從Ｆ拿去非空 2.再空
  // 沒空 1.

  // 1大＋順序小([8,7,6,5][4][3,2,1])：開始套用 honei
  // 套用完再加1即可再套用。
  // 結案。

  return step[num]
}

